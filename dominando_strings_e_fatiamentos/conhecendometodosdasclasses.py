# MAIÚSCOLO, munúscolo e Título #

curso = " pYtHoN"

print(curso.upper()) #  convertte para MAIÚSCULO #

print(curso.lower()) # converte para minuúsculo #

print(curso.title()) # apenas a primeira letra em MAIÚSCOLO #

# Eliminando espaços em branco #

curso = "    Python "

print(curso.strip()) #Remove todos os espaços#

print(curso.lstrip()) #remove os espaços da esquerda#

print(curso.rstrip()) #remove os espaços da direita#

"""Junções e centralização"""

curso = "Python"

print(curso.center(20, "*"))

print(".".join(curso))


